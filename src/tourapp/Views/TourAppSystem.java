    private void saveTPbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveTPbtnActionPerformed
        // TODO add your handling code here:
        
        //Save new tour package
        String location= "";
       
        if(!TPLocationField.getText().isEmpty() || !TPLocationField.getText().isBlank())
        {
           location = TPLocationField.getText();
        }
        else{
            JOptionPane.showMessageDialog(null, "Please fill Tour Package location field","warning",2);
        }
        
        double tourPackageCost = 0;
        if(!TPCostField.getText().isBlank() || !TPCostField.getText().isBlank()){
            tourPackageCost = Double.parseDouble(TPCostField.getText());
        }
        
        ArrayList<TourPackage> tourPackages = new ArrayList<TourPackage>(); 
        
        //radio button selection for new tour package
        
        //city tour RadioButton is selected
        if (cityRadioButton.isSelected()){
             if(!guideCostField.getText().isBlank() || !guideCostField.getText().isBlank()){
                 double guideCost = Double.parseDouble(guideCostField.getText());
                 
            boolean withGuide = false;
            if(yesRadioButton.isSelected()){
                withGuide = true;
            
                //select if withGuide is true
                TPDescriptionField.setText("City Tour");
                CityTour cityTour = new CityTour(withGuide, guideCost, "city tour", location, tourPackageCost);
                tourPackages.add(cityTour);
                TPlistbox.addItem(cityTour.toString());
            }
            
                
            else if(noRadioButton.isSelected()){   
                //select if withGuide is false
                TPDescriptionField.setText("City Tour");
                CityTour cityTour = new CityTour(withGuide, guideCost, "city tour", location, tourPackageCost);
                tourPackages.add(cityTour);
                TPlistbox.addItem(cityTour.toString());
            }
            else{
                JOptionPane.showMessageDialog(null, "Please select if with guide or not","warning",2);
                }
            
                }
            }
          
        //sightseeing RadioButton is selected
        else if (sightseeingRadioButton.isSelected()){
            
            if(!ticketCostField.getText().isBlank() || !ticketCostField.getText().isBlank()){
            double ticketCost = Double.parseDouble(ticketCostField.getText());
            
            TPDescriptionField.setText("Sightseeing Tour"); 
            SightseeingTour sightseeingTour = new SightseeingTour(ticketCost, "Sightseeing tour", location, tourPackageCost);
            tourPackages.add(sightseeingTour);
            TPlistbox.addItem(sightseeingTour.toString());
            }
            
            
        }
        //Error message if no tour package is selected
        else{
                JOptionPane.showMessageDialog(null, "Please select a tour package","warning",2);
            }
    }//GEN-LAST:event_saveTPbtnActionPerformed

