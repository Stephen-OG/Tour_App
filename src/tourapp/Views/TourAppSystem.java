    private void createBookingbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBookingbtnActionPerformed
        // TODO add your handling code here:
        //Create a new booking for a tourist(s)
        
        Tourist tourist;
        TourPackage tourPackage;
        Date date = jDateChooser1.getDate();
        
        //Select a tourist
        int touristSelectedIndex =  touristListbox.getSelectedIndex();
        tourist = tourists.get(touristSelectedIndex);
        
        //Select a tour package
        int tourPackageSelectedIndex =  TPlistbox.getSelectedIndex();
        tourPackage = tourPackages.get(tourPackageSelectedIndex);
         
        //Error messages for not selecting either a tourist or a tour package or not putting date
        if(tourist == null)
        {
            JOptionPane.showMessageDialog(null, "Please Select a Tourist","warning",2);
        }
        
        if(tourPackage == null)
        {
           JOptionPane.showMessageDialog(null, "Please Select a Tour Package","warning",2);
        }
        
        if(date == null)
        {
           JOptionPane.showMessageDialog(null, "Please Select a date","warning",2);
        }
        
        //add booking to booking list combo-box
        Booking newBooking = new Booking(tourist, tourPackage, date);
        bookings.add(newBooking);
        bookingslist.addItem(newBooking.toString());
    }//GEN-LAST:event_createBookingbtnActionPerformed

