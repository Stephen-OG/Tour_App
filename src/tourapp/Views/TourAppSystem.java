    private void saveTouristbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveTouristbtnActionPerformed
        // TODO add your handling code here:
        
        //Save new Tourist
        String name= "";
        String country= "";
        int selectedAudioIndex =  audioGuideBox.getSelectedIndex();
        
        //Error messages for skipping any input field
         if(!touristNameField.getText().isEmpty() || !touristNameField.getText().isBlank())
        {
            name = touristNameField.getText();
        }
         else{
             JOptionPane.showMessageDialog(null, "Please fill Tourist Name", "warning",2);
         }
        
        if(!touristCountryField.getText().isEmpty() || !touristCountryField.getText().isBlank())
        {
           country = touristCountryField.getText();
        }
        else{
            JOptionPane.showMessageDialog(null, "Please fill Tourist Nationality ","warning",2);
        }
        
        //add new tourist to tourist list combo-box
        audioGuideBox.getItemAt(selectedAudioIndex);
        audioLanguages.add(selectedAudioIndex, AudioLanguage.ENGLISH);
        Tourist tourist = new Tourist(name, country, audioLanguages.get(selectedAudioIndex));
        tourists.add(tourist);
        touristListbox.addItem(tourist.toString());
        
    }//GEN-LAST:event_saveTouristbtnActionPerformed

